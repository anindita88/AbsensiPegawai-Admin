package model;

/**
 *
 * @author Anindita
 */
public class ModelPegawai {
    private String idpegawai;
    private String nama;
    private String jabatan;
    private String foto;
    private String tampung_jabatan;
    private String shift;

    /**
     * @return the idpegawai
     */
    public String getIdpegawai() {
        return idpegawai;
    }

    /**
     * @param idpegawai the idpegawai to set
     */
    public void setIdpegawai(String idpegawai) {
        this.idpegawai = idpegawai;
    }
    
    /**
     * @return the nama
     */
    public String getNama() {
        return nama;
    }

    /**
     * @param nama the nama to set
     */
    public void setNama(String nama) {
        this.nama = nama;
    }

    /**
     * @return the jabatan
     */
    public String getJabatan() {
        return jabatan;
    }

    /**
     * @param jabatan the jabatan to set
     */
    public void setJabatan(String jabatan) {
        this.jabatan = jabatan;
    }

    /**
     * @return the foto
     */
    public String getFoto() {
        return foto;
    }

    /**
     * @param foto the foto to set
     */
    public void setFoto(String foto) {
        this.foto = foto;
    }

    /**
     * @return the tampung_jabatan
     */
    public String getTampung_jabatan() {
        return tampung_jabatan;
    }

    /**
     * @param tampung_jabatan the tampung_jabatan to set
     */
    public void setTampung_jabatan(String tampung_jabatan) {
        this.tampung_jabatan = tampung_jabatan;
    }

    /**
     * @return the shift
     */
    public String getShift() {
        return shift;
    }

    /**
     * @param shift the shift to set
     */
    public void setShift(String shift) {
        this.shift = shift;
    }

    
     
}
